  public void Search(){
      String name=this.txtFind.getText();
      if(name.isEmpty()){
          JOptionPane.showMessageDialog(this,"Nhap ma hang can tim");
      }
      DefaultTableModel model= (DefaultTableModel) this.tblMenu.getModel();
      int index=-1;
      for(int i=0;i<model.getRowCount();i++){
          String mh = model.getValueAt(i, 1).toString();
          if(mh.equalsIgnoreCase(name)){
              index=i;
              break;
          }
      }
      if(index!=-1){
          JOptionPane.showMessageDialog(this, "FOUND");
          int viewIndex = tblMenu.convertRowIndexToView(index);
         tblMenu.setRowSelectionInterval(viewIndex, viewIndex);
        tblMenu.scrollRectToVisible(tblMenu.getCellRect(viewIndex, 0, true));
      } else {
          JOptionPane.showMessageDialog(this,"NOT FOUND " + name);
      }
      
  }
