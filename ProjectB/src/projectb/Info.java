    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        String Hoten=this.txtName.getText();
        String Gioitinh=this.txtGender.getText();
        int Namsinh=Integer.parseInt(this.txtYear.getText());
        String DiaChi=this.txtAddress.getText();
        int hesoluong=Integer.parseInt(this.txtSalary.getText());
        boolean isYes=ManagerCheckBox.isSelected();
        if(isYes){
            int HeSoChucVu=Integer.parseInt(this.txtChucVu.getText());
            list.add(new QuanLy(HeSoChucVu,Hoten, Namsinh, Gioitinh, DiaChi, hesoluong));
        } else {
            String NganhDaoTao=this.txtNganhDaoTao.getText();
            list.add(new NhanVien(NganhDaoTao, Hoten, Namsinh, Gioitinh, DiaChi, hesoluong));
        }
        ViewTable();
    }//GEN-LAST:event_btnLuuActionPerformed
       public void TXT(){
           try(BufferedWriter bw = new BufferedWriter(new FileWriter("canbo.txt"))){
               for(CanBo x: list){
                   if (x instanceof NhanVien) {
                    NhanVien nv = (NhanVien) x;
              String line= x.getHoTen() + "," + x.getGioiTinh() + "," + x.getDiaChi()+ "," + x.getNamSinh() + "," + ((NhanVien) x).getNganhDaoTao() + "," + x.TinhLuong();
              bw.write(line);
              bw.newLine();
                   } else if (x instanceof QuanLy) {
                       QuanLy ql = (QuanLy) x;
                        String line= x.getHoTen() + "," + x.getGioiTinh() + "," + x.getDiaChi()+ "," + x.getNamSinh() + "," + x.TinhLuong();
                        bw.write(line);
                      bw.newLine();
               }
           }
       JOptionPane.showMessageDialog(null, "Lưu file thành công!");
    } catch (IOException e) {
        JOptionPane.showMessageDialog(null, "Lỗi khi lưu file: " + e.getMessage());
    }
}
