    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String HoTen=this.txtHoTen.getText();
        String QueQuan=this.txtQueQuan.getText();
        int thamNien = Integer.parseInt(txtThamNien.getText());
        int soGio = Integer.parseInt(txtSoGio.getText());
        boolean isBepTruong=ChefCheckBox.isSelected();
        boolean isThuNgan=NhanvienCheckBox.isSelected();
        if(isBepTruong){
            String input= JOptionPane.showInputDialog(null,"Nhập thưởng chức vụ: ");
            double thuongChucVu=Double.parseDouble(input);
            list.add(new NhanVienBep(true, HoTen, thamNien, QueQuan, soGio, thuongChucVu));
        } else if(isThuNgan){
            boolean isNgoaiNgu= NgoaiNguCheckBox.isSelected();
            list.add(new NhanVienThuNgan(isNgoaiNgu,HoTen, thamNien, QueQuan, soGio));
        } else {
            list.add(new NhanVien(HoTen, thamNien, QueQuan, soGio));
        }
       View();
       ViewTable();
    }//GEN-LAST:event_btnSaveActionPerformed

