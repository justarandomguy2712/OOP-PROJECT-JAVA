    public void ViewTable() {
    DefaultTableModel model = (DefaultTableModel) this.tblhaha.getModel();
    model.setNumRows(0);
    int n = 1;

    for (CanBo x : list) {
        if (x instanceof NhanVien) {
            NhanVien nv = (NhanVien) x;
            model.addRow(new Object[]{
                n++, nv.getHoTen(), nv.getGioiTinh(), nv.getNamSinh(),
                nv.getDiaChi(), nv.getHeSoLuong(),
                nv.getNganhDaoTao(),   
                nv.TinhLuong()      
            });
        } else if (x instanceof QuanLy) {
            QuanLy ql = (QuanLy) x;
            model.addRow(new Object[]{
                n++, ql.getHoTen(), ql.getGioiTinh(), ql.getNamSinh(),
                ql.getDiaChi(), ql.getHeSoLuong(),
                 "",             
                ql.TinhLuong()     
            });
        }
        }
    }


  
