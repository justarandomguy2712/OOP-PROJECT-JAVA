public void SaveFile(){
    try(BufferedWriter bw = new BufferedWriter(new FileWriter("nhanvien.txt"))){
        for(NhanVien x: list){
            String line= x.getHoTen()+ "," + x.getQưeQuan() + "," + x.getSoGio() + "," + x.getThamNien()+"," +x.tinhLuong();
            bw.write(line);
            bw.newLine();
        }
        JOptionPane.showMessageDialog(null, "Lưu file thành công!");
    } catch(IOException e){
        JOptionPane.showMessageDialog(null, "KO Thanh cong" + e.getMessage());
    }
}
