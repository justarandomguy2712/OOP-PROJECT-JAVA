    public void ReadFile(){
        try(BufferedReader br = new BufferedReader(new FileReader("mathang.txt"))){
            list.clear();
            String line;
            while ((line = br.readLine()) != null){
            String [] part = line.split(",");
            if(part.length==4){
            String Mathang=part[0].trim();
            String Mahang=part[1].trim();
            int Soluong=Integer.parseInt(part[2].trim());
            int Giavon=Integer.parseInt(part[3].trim());
            MatHang mh = new MatHang(Mathang,Mahang,Soluong,Giavon);
            list.add(mh);
            }
            if(part.length==5){
             boolean trinhdien=Boolean.parseBoolean(part[0].trim());
             String Mathang=part[1].trim();
             String Mahang=part[2].trim();
              int Soluong =Integer.parseInt(part[3].trim());
             int Giavon=Integer.parseInt(part[4].trim());
            DoUong du = new DoUong(trinhdien,Mathang,Mahang,Soluong,Giavon);
            list.add(du);
            }
        } 
            ViewTable();
                
     } catch (Exception e){
             JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
             }
        }
