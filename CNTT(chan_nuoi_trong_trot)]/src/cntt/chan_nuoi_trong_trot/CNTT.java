    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        String id=this.txtMaRuong.getText();
        String name=this.txtName.getText();
        String ruong=this.txtCaytrong.getText();
        int chieurong=Integer.parseInt(this.txtChieuRong.getText());
        int chieudai=Integer.parseInt(this.txtChieuDai.getText());
        if(chieurong==chieudai){
            list.add(new RuongVuong(chieudai,id,name,ruong));
        } else {
            list.add(new RuongChuNhat(chieudai,chieurong,id, name,ruong));
        }
        ViewTable();
        JOptionPane.showMessageDialog(this, "Saved!!!");
    }//GEN-LAST:event_btnLuuActionPerformed
     public void Save(){
         try(BufferedWriter bw = new BufferedWriter(new FileWriter("ruong.txt"))){
             for(Ruong x: list){
                 String line=x.getName()+ "," + x.getID() + "," +x.getLoaicaytrong()+ "," + x.tinhDienTich();
                 bw.write(line);
                 bw.newLine();
                 
             }
             JOptionPane.showMessageDialog(this, "Luu File Thanh Cong");
         } catch (Exception e){
             JOptionPane.showMessageDialog(this, "Ko thanh cong"+e.getMessage());
         }
     }
