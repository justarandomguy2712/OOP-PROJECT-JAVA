    public void ViewTable(){
        int countCN = 0;
        int countVuong = 0;
        int countDienTich=0;
     Map<String,Integer> dc= new HashMap<>();
        for (Ruong r : list) {
            if (r instanceof RuongVuong) {
                countVuong++;
            } else if (r instanceof RuongChuNhat) {
                countCN++;
            }
            if(r.tinhDienTich()>100) {
                countDienTich++;
            }
            String cay=r.getLoaicaytrong();
            dc.put(cay, dc.getOrDefault(cay,0)+1);
        }
        String cayMax="Khong co";
        int max=0;
        for(Map.Entry<String, Integer> entry: dc.entrySet()){
            if(entry.getValue()>max){
                max=entry.getValue();
                cayMax=entry.getKey();
            }
        }
   
        DefaultTableModel model = (DefaultTableModel) this.tblHaha.getModel();
        model.setRowCount(0);
        int n = 1;
        model.addRow(new Object[]{n++,cayMax,countDienTich, countCN, countVuong});
    }
